let currentLang: Lang = 'zh';

export function getLang(): Lang {
  return currentLang;
}

export function setLang(lang: Lang): void {
  currentLang = lang === 'en' ? 'en' : 'zh';
  try {
    localStorage.setItem('btc-dashboard-lang', currentLang);
  } catch {
    /* localStorage unavailable — ignore */
  }
}

/** Load the persisted language (called once at boot). */
export function initLang(): Lang {
  try {
    const saved = localStorage.getItem('btc-dashboard-lang');
    if (saved === 'en' || saved === 'zh') currentLang = saved;
  } catch {
    /* ignore */
  }
  return currentLang;
}

/** Translate a key, interpolating `{param}` placeholders. Falls back to the
 *  Chinese string, then the key itself, so a missing key is visible, not blank. */
export function t(key: string, params?: Record<string, string | number>): string {
  let s = DICT[currentLang][key] ?? DICT.zh[key] ?? key;
  if (params) {
    for (const k of Object.keys(params)) {
      s = s.split(`{${k}}`).join(String(params[k]));
    }
  }
  return s;
}

